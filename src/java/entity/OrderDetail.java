package entity;

/**
 *
 * @author DEKUPAC
 */
public class OrderDetail {

    private int orderDetailId, orderId, productId;
    private String orderDate;
    private int amount, cost;

    public OrderDetail() {
    }

    public OrderDetail(int orderDetailId, int orderId, int productId, String orderDate, int amount, int cost) {
        this.orderDetailId = orderDetailId;
        this.orderId = orderId;
        this.productId = productId;
        this.orderDate = orderDate;
        this.amount = amount;
        this.cost = cost;
    }

    public int getOrderDetailId() {
        return orderDetailId;
    }

    public void setOrderDetailId(int orderDetailId) {
        this.orderDetailId = orderDetailId;
    }

    public int getOrderId() {
        return orderId;
    }

    public void setOrderId(int orderId) {
        this.orderId = orderId;
    }

    public int getProductId() {
        return productId;
    }

    public void setProductId(int productId) {
        this.productId = productId;
    }

    public String getOrderDate() {
        return orderDate;
    }

    public void setOrderDate(String orderDate) {
        this.orderDate = orderDate;
    }

    public int getAmount() {
        return amount;
    }

    public void setAmount(int amount) {
        this.amount = amount;
    }

    public int getCost() {
        return cost;
    }

    public void setCost(int cost) {
        this.cost = cost;
    }

    @Override
    public String toString() {
        return "OrderDetail{" + "orderDetailId=" + orderDetailId + ", orderId=" + orderId + ", productId=" + productId + ", orderDate=" + orderDate + ", amount=" + amount + ", cost=" + cost + '}';
    }
    
}
